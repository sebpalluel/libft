#include "libft.h"

void		ft_matrixonpoint(double **m, t_vec3 *vec3)
{
	if (m && vec3)
	{
		vec3->x = m[0][0] * vec3->x
			+ m[0][1] * vec3->y
			+ m[0][2] * vec3->z + m[0][3];
		vec3->y = m[1][0] * vec3->x
			+ m[1][1] * vec3->y
			+ m[1][2] * vec3->z + m[1][3];
		vec3->z = m[2][0] * vec3->x
			+ m[2][1] * vec3->y
			+ m[1][2] * vec3->z + m[2][3];
	}
}
